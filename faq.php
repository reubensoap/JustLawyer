                    <h2 class="faq-header" id="A1">About the Lawyer Finder Directory</h2>
                    <h3 id="A2">What is the Lawyer Finder Directory</h3>
                    <p>The Justia Lawyer Directory is a free online resource that provides detailed profiles of lawyers, law firms, and non-profit legal service and legal aid organizations in the United States.
                        Any lawyer or organization can have a complete free profile on the Justia Lawyer Directory.</p>
                    <h2 id="B1">Finding an Attorney</h2>
                    <h3 id="B2">How do I find an attorney using the Lawyer Finder Directory?</h3>
                    <p>Start your search for an attorney on the Justia Lawyer Directory home page. 
                    In the search box, enter a legal issue or the name of a lawyer or law firm 
                    and the geographic location you would like to search and click on the search 
                    button. Your search results will include a list of local attorneys, law firms, 
                    and non-profit legal service organizations that meet your search criteria.
                    Note that if we have your approximate geographic location, 
                    it will be pre-populated in the search box at the top of the page. 
                    You may also edit the geographic location to search for an attorney 
                    in a specific city, county or state. You can also browse attorneys in the Justia Lawyer Directory. 
                    Scroll down the page to browse attorneys by Practice Area, State or City. </p>
                    <h3 id="B3">How do you know where I am located?</h3>
                    <p>The Justia Lawyer Directory uses a technology known as IP Geolocation
                    to determine your general location. We do this so that when you visit
                    the site, we can direct you to attorneys in your area as quickly as 
                    possible. We do not store your location on our servers, or use this 
                    information to identify you. We only use it to simplify and enhance 
                    your experience with the Justia Lawyer Directory.</p>
                    <h3 id="B4">How can I find free or low cost legal services or legal aid?</h3>
                    <p>The Justia Lawyer Directory lists thousands of educational and non-profit 
                    legal aid and service organizations nationwide that provide free or 
                    low cost legal services. If you are interested in these low cost legal 
                    service organizations, search or browse the lawyer directory for the area of law 
                    and geographic location in which you are interested. In the right sidebar on every 
                    listing page there will be a short list of available Legal Aid & Pro Bono services that 
                    matches your search criteria. To see more providers in your area, click on "Show More."</p>
                    <h3 id="B5">How do I contact an attorney in your directory?</h3>
                    <p>There are several different ways to contact lawyers, law firms and legal aid &
                    service organizations listed in the Justia Lawyer Directory: </p>
                    <ul>
                        <li><p class="tab2"><span class="bold">Use the Contact Information in Their Profile:</span> On a profile page, 
                        you can click on the "Contact & Map" button to see phone numbers 
                        and addresses the attorney has listed on his or her profile, 
                        as well as a map showing all of their office locations. </p></li>
                        <li><p class="tab2"><span class="bold">Use the Email Form:</span> If the attorney or law firm has a contact email 
                        address associated with their profile, you can click on the “Contact & Map” button and click on the 
                        “Email” link just below the map. A form will appear where you can send your message directly to the 
                        attorney. For the privacy and security of the attorneys in our database, we do not display their email 
                        addresses anywhere on our site. </p></li>
                        <li><p class="tab2"><span class="bold">Visit Their Website:</span> Many lawyers and law firms have included links 
                        to their websites and/or blogs where you can find more information about the lawyer, including additional 
                        contact methods. These website and blog links can be found by clicking on the “Website & Blog” button. 
                        Blogs offer an excellent way to learn more about a lawyer, their practice, and the law in general. If an 
                        attorney or firm has included a link to their blog in their profile, you will be able to view their recent 
                        posts directly in the profile. </p></li>
                    </ul>
                    <h2 id="C1">Claiming or Registering Your Attorney Profile</h2>
                    <h3 id="C2">Can I be listed in the Lawyer Finder Directory?</h3>
                    <p>Any attorney licensed in the United States can have a free profile in the Justia Lawyer Directory. 
                        Free profiles are full-featured, and include the ability to add a photo, biographical information, 
                        education and experience, websites and blogs, social media profiles, and more. If there is already 
                        a profile for you in the Justia Lawyer Directory, you can claim it by clicking the red “Update your 
                        profile now” link directly below your name on your profile page. If there is not already a profile for you, 
                        you may create a new one here. </p>
                    <h3 id="C3">Is there a fee to be listed in the Lawyer Directory?</h3>
                    <p>No. There is no fee to be listed and have a complete profile in the Justia Lawyer Directory. </p>
                    <h3 id="C4">Why should I claim my attorney profile?</h3>
                    <p>The Justia Lawyer Directory provides free profiles to lawyers throughout the United States. </p>
                    <p>Registered lawyers may:</p>
                    <ol>
                        <li><p class="tab2">1. Complete their profile to show the full extent of their professional experience. </p></li>
                        <li><p class="tab2">2. Help users find your Website and Blog or other social network and directory profiles, such as Facebook, LinkedIn, JDSupra, Twitter, and Avvo. </p></li>
                        <li><p class="tab2">3. Receive online leads from potential clients. </li>
                        <li><p class="tab2">4. Receive community participation by providing a fuller, more detailed and complete profile.</p> </li>
                        <li><p class="tab2">5. Receive community participation and higher listings by providing free legal information through blogs, wikis, and forums.</p></li>
                        <li><p class="tab2">6. Receive community participation and higher listings by supporting organizations, such as Cornell's Legal Information Institute, that provide free legal information. </p></li>
                        <li><p class="tab2">7. Be included in other lawyer directories, such as the lawyer directory of the Legal Information Institute, that only lists those lawyers who have been authenticated. </p></li>
                    </ol>
                    <h3 id="C5">How can I get listed in the directory? </h3>
                    <p> You may already have a listing in the directory. Search for your name using the search box at the top of the page. If one of the results is you, 
                        click on the red “Update your profile now!” link on the profile page immediately below your name.
                        <br/>If you cannot find your profile in the Justia Lawyer Directory, you may register as a new attorney by clicking on the “Get Listed” link located on the top right of every page. </p>
                    <h3 id="C6">Can someone else manage my profile?</h3>
                    <p>It is Justia's policy that each claimed lawyer directory profile in our directory must be 
                        associated with the corresponding attorney's email address. The person claiming or updating 
                        must have access to the lawyer's personal law firm email address.<br/><br/>
                        You must use a personal firm email address to claim and update your free profile. 
                        Your ability to update your profile may be revoked if you claim your profile using 
                        an email other than your personal firm email address (such as a generic office email 
                        address). If a firm email address is not available, please contact us and we can assist 
                        in getting the profile claimed.<br/><br/>
                        If you would like a third-party to claim your profile on your behalf or if you are a 
                        third-party looking to claim a profile, please refer to “Claiming on behalf of an attorney” below.<br/><br/>
                        If you have multiple profiles to claim for one firm, please see “Claiming multiple profiles for your firm” below. </p>
                    <h2 id="D1">Editing/Completing Your Attorney Profile</h2>
                    <h3 id="D2">How do I login?</h3>
                    <p>To login, click the "Sign In" link at the top right of every page. Use the login and password you 
                        created when you claimed your profile. If you lose your password you may reset it on the password 
                        reset page. If you forgot your password you may reset it on the password reset page, here.</p>
                    <h3 id="D3">How do I update information in my lawyer profile?</h3>
                    <p> If you would like to update information in your lawyer profile, you must first claim your profile. 
                        Once your profile has been claimed, you may edit all information in your profile by following the 
                        instructions below.<br/><br/>
                        <span class="bold">The Justia Lawyer Directory team will not respond to requests by email or phone 
                            to change information listed in unclaimed profiles. This is to ensure that only lawyers who 
                            own the profile can change the information within it.</span> </p>
                    <h3 id="D4">How do I edit my profile?</h3>
                    <p>Once you have claimed/created your profile, you can log in and edit your profile simply by visiting it. 
                        To visit your profile click the "My Account" link at the top right of any page. On your account page, 
                        click on the link to the profile you wish to edit. You will see a large, blue “Edit your profile” button. </p>
                    <h3 id="D5">How can I remove my profile from the Justia Lawyer Directory? </h3>
                    <p> If you do not want your lawyer profile to be visible, you must first claim your profile. 
                        On your profile page, click on the "Claim and Update your profile" link on the top right 
                        of your profile and complete the claiming process. Once your profile has been claimed, you 
                        may either disable or delete your profile from the Lawyer Directory.<br/><br/>
                        To hide or delete a profile, navigate to the editing interface of your profile 
                        and click on the "Settings" button. If you select "Disable," your profile will 
                        remain hidden until you re-enable it. You may also permanently delete your profile 
                        by selecting “Delete.” Data contained in a deleted profile cannot be restored. 
                        The difference between disabling and deleting your profile is described further below. </p>
                    <h3 id="D6">What is the difference between disabling my profile and deleting my profile? </h3>
                    <p> Both disabling and deleting your profile remove it from the public listings in the Justia Lawyer Directory.<br/><br/>
                    Disabling your profile is a reversible action. It allows you to hide your profile until you decide 
                    you want to show it again. You can still update your hidden profile, but it is not visible to 
                    the general public. You can make a disabled profile visible on the Justia Lawyer Directory at 
                    any time by re-enabling it.<br/><br/>
                    Deleting your profile will remove your information from the Justia Lawyer Directory permanently 
                    and you will no longer be able to login, edit or update your profile. If you want to re-add your 
                    profile after deleting it, you will have to create a brand new profile and re-enter the relevant information. </p>
                    <h3 id="D7">How can I change my password? </h3>
                    <p>After you log in, click on the "My Account" link in the upper right-hand corner to see your accounts page. 
                        Underneath the “Account Information” heading on your accounts page, click on the “Change Password” link 
                        and follow the prompts to change your password. </p>
                    <h3 id="D8">What are the ideal dimensions for an attorney photo? </h3>
                    <p>You should upload a photo that is a minimum of 640px by 640px for your attorney profile photo. 
                        If you upload a photo with different dimensions, the editing tool will resize the photo to fit within these 
                        dimensions. We accept photos in JPG format only. </p>
                    <h3 id="D9">What type of photo should I upload to my profile? </h3>
                    <p>You should upload a professional photo with your headshot or another image with your likeness. 
                        Photos that include law firm or third-party branding, animals, landscapes, plants, objects or 
                        other text may be removed at our discretion. Photos should not have a frame, colored border or 
                        other artificial border. Presenting a professional photo builds trust in a lawyer and confidence 
                        in the credentials presented in the profile. As an added benefit, your photo is also included in 
                        the geographical and practice area listings of the directory, and can help distinguish your profile 
                        from those of your peers who have not yet added a photo. </p>
